package obe;

public class LocalVariable extends Obe {
	
	private String type;
	
	public LocalVariable(String name, String type) {
		this.setName(name);
		this.type = type;
	}
	
	public String getName() {
		return this.getName();
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
	
	
	
}
